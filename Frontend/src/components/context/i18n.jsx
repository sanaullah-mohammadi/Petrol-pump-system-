// ─── Pashto numeral converter ────────────────────────────────────────────────
const _PS_DIGITS = "۰۱۲۳۴۵۶۷۸۹";
/** Convert Pashto/Eastern-Arabic numerals to ASCII digits */
export function parsePashtoNum(str) {
  if (str == null) return str;
  return String(str).replace(/[۰-۹]/g, (d) => _PS_DIGITS.indexOf(d));
}
/** Parse a value that may contain Pashto numerals into a JS number */
export function toArabicNum(val) {
  const cleaned = parsePashtoNum(String(val ?? "")).replace(/[^0-9.-]/g, "");
  const n = parseFloat(cleaned);
  return isNaN(n) ? 0 : n;
}

// ─── Currency formatter ───────────────────────────────────────────────────────
/**
 * Format a monetary amount by UI language.
 * English:  "500 AFN"  (number + space + AFN)
 * Pashto:   "500 ؋"    (number + space + ؋)
 */
export function fmtCurrency(amount, lang, decimals) {
  const l = lang || "ps";
  const d = decimals ?? 2;
  const n = Number(amount);
  const zero = `0.${"0".repeat(d)}`;
  if (amount == null || isNaN(n))
    return l === "ps" ? `${zero} ؋` : `${zero} AFN`;
  const formatted = n.toLocaleString("en", {
    minimumFractionDigits: d,
    maximumFractionDigits: d,
  });
  return l === "ps" ? `${formatted} ؋` : `${formatted} AFN`;
}

/**
 * Format a monetary amount by its stored currency code.
 * USD  → "$500.00"   ($ prefix, no space)
 * AFN  → "500 AFN"   (number + space + AFN)
 * Default: same as AFN
 */
export function fmtByCurrency(amount, currencyCode, decimals) {
  const d = decimals ?? 2;
  const n = Number(amount);
  const zero = `0.${"0".repeat(d)}`;
  if (amount == null || isNaN(n))
    return currencyCode === "USD" ? `$${zero}` : `${zero} AFN`;
  const formatted = n.toLocaleString("en", {
    minimumFractionDigits: d,
    maximumFractionDigits: d,
  });
  return currencyCode === "USD" ? `$${formatted}` : `${formatted} AFN`;
}

/** @deprecated use fmtCurrency(amount, lang) */
export function fmtAFN(amount, decimals) {
  return fmtCurrency(amount, "ps", decimals);
}
/** Get currency symbol for current language */
export function currencySymbol(lang) {
  return lang === "ps" ? "؋" : "AFN";
}

